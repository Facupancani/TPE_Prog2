
public class nodo {

	private String nombre;
	private String apellido;
	private int edad;
	private int DNI;
	private String intereses;
	
	public nodo(String Nombre, String Apellido, int Edad, int DNI, String Intereses){
		this.nombre = Nombre;
		this.apellido = Apellido;
		this.edad = Edad;
		this.DNI = DNI;
		this.intereses = Intereses;
	}
}
