
public class nodo {

	 String nombre;
	 String apellido;
	 int edad;
	 int DNI;
	 String intereses;
	
	public nodo(String Nombre, String Apellido, int Edad, int DNI, String Intereses){
		this.nombre = Nombre;
		this.apellido = Apellido;
		this.edad = Edad;
		this.DNI = DNI;
		this.intereses = Intereses;
	}
}
